#include<bits/stdc++.h>
using namespace std;

int main ()
{
    int arr[10];
    arr[0] = 2;
    arr[1] = 4;

    cout << "arr : " << arr << endl;
    cout << "&arr[0] : " << &arr[0] << endl;
    cout << "*arr " << *arr << endl;
    cout << "a + 1 : " << arr + 1 << endl;
    cout << "&arr[1] : " << &arr[1] << endl; 
    cout << "*(a+1) : " << *(arr+1) << endl;



}